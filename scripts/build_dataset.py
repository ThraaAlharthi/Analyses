def comparison(a, b):
    area, lat, lon = a[0], a[1], a[2]
    d1, m1, v1 = a[3], a[4], a[7]
    d2, m2, v2 = b[3], b[4], b[7]
    dm, dv = round(m2 - m1, 2), round(v2 - v1, 1)
    payload = {
        "area_name_ar": area,
        "observations": [
            {"date": d1, "ndvi_mean": m1, "vegetation_percent": v1},
            {"date": d2, "ndvi_mean": m2, "vegetation_percent": v2},
        ],
        "delta_ndvi_mean": dm, "delta_vegetation_percent": dv,
        "latitude": lat, "longitude": lon,
    }
    if dm > 0:
        trend = "تحسّنًا في حالة الغطاء النباتي"
        tail = "يتوافق هذا الاتجاه مع دخول موسم نمو أو زيادة في الريّ."
    else:
        trend = "تراجعًا في حالة الغطاء النباتي"
        tail = "قد يعود هذا التراجع إلى الإجهاد الحراري أو انخفاض توفر المياه."
    out = (f"في {area}، انتقل متوسط NDVI من {n(m1)} بتاريخ {d1} إلى {n(m2)} بتاريخ {d2}، "
           f"أي بفارق قدره {n(dm)}، ما يعكس {trend}. "
           f"وتغيّرت نسبة التغطية النباتية من {n(v1)}% إلى {n(v2)}%، بفارق {n(dv)} نقطة مئوية. "
           f"{tail}")
    return {"instruction": CMP_INSTR,
            "input": json.dumps(payload, ensure_ascii=False), "output": out}

