def single(i, row):
    area, lat, lon, date, mean, mn, mx, veg, px = row
    payload = {
        "image_id": f"{SLUG[area]}_{date.replace('-', '_')}.tif",
        "area_name_ar": area, "date": date,
        "ndvi": {"mean": mean, "min": mn, "max": mx},
        "vegetation_percent": veg, "pixel_count": px,
        "latitude": lat, "longitude": lon,
    }
    out = " ".join([
        S1[i % 4].format(area=area, date=date, mean=n(mean), reg=regime(mean)),
        S2[i % 3].format(mn=n(mn), mx=n(mx), spread=SPREAD[i % 3]),
        S3[i % 3].format(veg=n(veg)),
        advice(mean),
    ])
    return {"instruction": INSTR[i % 5],
            "input": json.dumps(payload, ensure_ascii=False), "output": out}

